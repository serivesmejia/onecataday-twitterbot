package com.github.serivesmejia.catadaybot;

public class Gato {
	
	public int ID;
	public String Nombre;
	public String Autor;
	public int Valor;
	public int Comida;
	
	public Gato(int ID, String Nombre, String Autor, int Valor, int Comida) {
		this.ID = ID;
		this.Nombre = Nombre;
		this.Autor = Autor;
		this.Valor = Valor;
		this.Comida = Comida;
	}
	
}
